    public void insertAddBookdetail(){
        String Title = title.getText();
        String Category = category.getText();
        String PublicationYear = pubyear.getText();
        
        Integer BookID = Integer.parseInt(bookid.getText());
        Integer AuthorID = Integer.parseInt(authorid.getText());
        Integer TotalCopies = Integer.parseInt(totalcopies.getText());
        Integer AvailableCopies = Integer.parseInt(availablecopies.getText());
        
        try{
            Connection con= DBConnect.getConnection();
            String query = "insert into book(BookID, AuthorID, Category, PublicationYear, AvailableCopies, TotalCopies, Title) values (?,?,?,?,?,?,?);";
            PreparedStatement ps = con.prepareStatement(query);
            
            ps.setInt(1, BookID);
            ps.setInt(2, AuthorID);
            ps.setString(3, Category);
            ps.setString(4, PublicationYear);
            ps.setInt(5, AvailableCopies);
            ps.setInt(6, TotalCopies);
            ps.setString(7, Title);
            
             int updateRowCount = ps.executeUpdate();
            
            if(updateRowCount != 0){
                JOptionPane.showMessageDialog(null,"Book Added","Add Book",3);
            }else{
                JOptionPane.showMessageDialog(null,"Book Not Added","Add Book",2);
            }
        }
         catch (SQLException ex) {
            Logger.getLogger(DBConnect.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
