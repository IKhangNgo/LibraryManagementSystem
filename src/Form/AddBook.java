    public boolean validateAddBook(){
        String Title = title.getText();
        String Category = category.getText();
        String PublicationYear = pubyear.getText();
       
        
       if(Title.trim().equals("")){
       JOptionPane.showMessageDialog(this,"Enter the title","Empty field",0);
       return false;
   }
       if(Category.equals("")){
       JOptionPane.showMessageDialog(this,"Enter the category","Empty field",0);
       return false;
   }
       if(PublicationYear.equals("")){
       JOptionPane.showMessageDialog(this,"Enter the publication year","Empty field",0);
       return false;
   }       
       return true;
    }

    

